package unboxing;

public class AutoUnboxing6 
{
	public static void main(String[] args) 
	{
		long x = 12;   //
		System.out.println(x);    //Widening OR Automatifc type casting
		
		Long a = 12L;
		System.out.println(a);
		
		Double b = 18.0;
		System.out.println(b);
		
		Double c = 21D;
		System.out.println(c);
		
		Float d = 67F;
		System.out.println(d);
	}

}
