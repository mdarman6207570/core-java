package sunday_day_02__Objects_and_its_Methods;

public class HashCodeDemo1 
{
	public static void main(String[] args) 
	{
		Integer i = 65;
		String str = "A";
		
		System.out.println("Integer object hashcode is :"+i.hashCode());
		System.out.println("String object hashcode is :"+str.hashCode());
	}

}
