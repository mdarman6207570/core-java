package day_16__Operators;

public class Operatorr 
{
	public static void main(String[] args) 
	{
        
		System.out.println(true^false);
		
	
		System.out.println(5 & 6);
		System.out.println(5 | 6);
		System.out.println(5 ^ 6);
		
		
		System.out.println(~5); //-6
		System.out.println(~ -4); //3
	}
}
